"""Narrator — LLM-powered narrative reasoning engine for Cortex.

Translates deterministic risk model outputs into human-readable insights
using an OpenAI-compatible API (Ollama, Together, OpenAI). This is NOT a
template engine — every response is generated by an LLM call with full
context injection.

Four capabilities:
  1. explain_decision  — synthesize guardian + debate + news → trade narrative
  2. interpret_news    — LLM interprets news items for crypto-specific impact
  3. market_briefing   — periodic market state summary from all risk models
  4. answer_question   — interactive Q&A about the system's current state

Design principles:
  - Never in the hot path — advisory only, never blocks trading
  - All calls async with timeout + graceful fallback
  - NARRATOR_ENABLED=false (default) → zero side-effect, no LLM calls
  - Reads from existing stores (debate_store, news_buffer, guardian, regime)
"""

from __future__ import annotations

__all__ = [
    "explain_decision",
    "interpret_news",
    "market_briefing",
    "answer_question",
    "get_narrator_status",
]

import logging
import time
from typing import Any

from cortex.config import (
    NARRATOR_ENABLED,
    NARRATOR_BASE_URL,
    NARRATOR_API_KEY,
    NARRATOR_MODEL,
    NARRATOR_MAX_TOKENS,
    NARRATOR_TEMPERATURE,
    NARRATOR_TIMEOUT,
)

logger = logging.getLogger(__name__)

# ── Lazy LLM client (OpenAI-compatible) ───────────────────────────────

_client: Any = None
_call_count: int = 0
_error_count: int = 0
_total_latency_ms: float = 0.0


def _get_client():
    """Lazy-initialize async OpenAI-compatible client on first use."""
    global _client
    if _client is not None:
        return _client

    from openai import AsyncOpenAI
    _client = AsyncOpenAI(
        base_url=NARRATOR_BASE_URL,
        api_key=NARRATOR_API_KEY or "ollama",
        timeout=NARRATOR_TIMEOUT,
    )
    return _client


async def _llm_call(system_prompt: str, user_prompt: str) -> str:
    """Make an async LLM call via OpenAI API. Returns generated text.

    Tracks call count, errors, and latency for observability.
    """
    global _call_count, _error_count, _total_latency_ms

    client = _get_client()
    t0 = time.time()

    try:
        response = await client.chat.completions.create(
            model=NARRATOR_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=NARRATOR_MAX_TOKENS,
            temperature=NARRATOR_TEMPERATURE,
        )
        _call_count += 1
        _total_latency_ms += (time.time() - t0) * 1000
        return response.choices[0].message.content or ""
    except Exception as exc:
        _error_count += 1
        _total_latency_ms += (time.time() - t0) * 1000
        logger.error("Narrator LLM call failed: %s", exc, exc_info=True)
        raise


# ── Context Collectors ──────────────────────────────────────────────────

def _collect_guardian_context(assessment: dict[str, Any]) -> str:
    """Format a guardian assessment dict into LLM-readable context."""
    lines = [
        f"Risk Score: {assessment.get('risk_score', 'N/A')}/100",
        f"Approved: {assessment.get('approved', 'N/A')}",
        f"Regime State: {assessment.get('regime_state', 'N/A')}",
        f"Recommended Size: ${assessment.get('recommended_size', 0):,.2f}",
        f"Confidence: {assessment.get('confidence', 0):.2%}",
    ]

    veto = assessment.get("veto_reasons", [])
    if veto:
        lines.append(f"Veto Reasons: {', '.join(veto)}")

    threshold = assessment.get("effective_threshold", 75)
    lines.append(f"Approval Threshold: {threshold}")

    scores = assessment.get("component_scores", [])
    if scores:
        lines.append("\nComponent Scores:")
        for cs in scores:
            comp = cs.get("component", "?")
            score = cs.get("score", 0)
            lines.append(f"  - {comp}: {score:.1f}/100")

    cb = assessment.get("circuit_breaker")
    if cb:
        lines.append(f"\nCircuit Breakers: {cb}")

    limits = assessment.get("portfolio_limits")
    if limits and limits.get("blocked"):
        lines.append(f"Portfolio Limits BLOCKED: {limits.get('blockers', [])}")

    return "\n".join(lines)


def _collect_debate_context(debate: dict[str, Any]) -> str:
    """Format a debate result dict into LLM-readable context."""
    if not debate:
        return "No debate data available."

    lines = [
        f"Final Decision: {debate.get('final_decision', 'N/A')}",
        f"Confidence: {debate.get('final_confidence', 0):.2%}",
        f"Rounds: {debate.get('num_rounds', 0)}",
        f"Decision Changed: {debate.get('decision_changed', False)}",
        f"Recommended Size: {debate.get('recommended_size_pct', 0):.1%}",
    ]

    evidence = debate.get("evidence_summary", {})
    if evidence:
        lines.append(f"\nEvidence: {evidence.get('bullish', 0)} bullish, {evidence.get('bearish', 0)} bearish")

        for item in evidence.get("bullish_items", [])[:3]:
            lines.append(f"  + {item.get('claim', '')}")
        for item in evidence.get("bearish_items", [])[:3]:
            lines.append(f"  - {item.get('claim', '')}")

    rounds = debate.get("rounds", [])
    if rounds:
        last_round = rounds[-1]
        arb = last_round.get("arbitrator", {})
        lines.append(f"\nFinal Arbitrator:")
        lines.append(f"  Approval Score: {arb.get('approval_score', 0):.2%}")
        for r in arb.get("reasoning", []):
            lines.append(f"  → {r}")

    return "\n".join(lines)


def _collect_news_context(news_signal: dict[str, Any] | None, news_items: list[dict] | None = None) -> str:
    """Format news signal and items into LLM-readable context."""
    if not news_signal:
        return "No news data available."

    lines = [
        f"Sentiment EWMA: {news_signal.get('sentiment_ewma', 0):.4f}",
        f"Direction: {news_signal.get('direction', 'NEUTRAL')}",
        f"Strength: {news_signal.get('strength', 0):.4f}",
        f"Confidence: {news_signal.get('confidence', 0):.4f}",
        f"Entropy: {news_signal.get('entropy', 0):.4f}",
        f"Items: {news_signal.get('n_items', 0)} from {news_signal.get('n_sources', 0)} sources",
        f"Distribution: {news_signal.get('bull_pct', 0):.0%} bull / {news_signal.get('bear_pct', 0):.0%} bear / {news_signal.get('neutral_pct', 0):.0%} neutral",
    ]

    if news_items:
        lines.append("\nTop Headlines:")
        for item in news_items[:5]:
            sent = item.get("sentiment", {})
            lines.append(
                f"  [{sent.get('label', '?')}] {item.get('title', 'N/A')} "
                f"(impact={item.get('impact', 0)}, source={item.get('source', '?')})"
            )

    return "\n".join(lines)


def _collect_regime_context() -> str:
    """Collect current regime state from model stores (best-effort)."""
    lines = []
    try:
        from api.stores import msm_store
        tokens = list(msm_store.keys()) if hasattr(msm_store, "keys") else []
        if tokens:
            lines.append(f"Calibrated tokens: {', '.join(tokens[:10])}")
            # Get regime for first token as representative
            import numpy as np
            for token in tokens[:3]:
                data = msm_store.get(token)
                if data and "filter_probs" in data:
                    fp = data["filter_probs"]
                    pi_t = np.asarray(fp.iloc[-1] if hasattr(fp, "iloc") else fp[-1])
                    regime = int(np.argmax(pi_t)) + 1
                    crisis_prob = float(pi_t[-1]) if len(pi_t) > 0 else 0.0
                    lines.append(f"  {token}: regime={regime}, crisis_prob={crisis_prob:.4f}")
    except Exception:
        lines.append("Regime data unavailable.")

    return "\n".join(lines) if lines else "No regime data available."


# ── DX-Research Context Collectors ────────────────────────────────────────

def _collect_stigmergy_context(token: str = "") -> str:
    """Collect pheromone board consensus for a token (or full snapshot)."""
    try:
        from cortex.stigmergy import get_consensus, get_board_snapshot
        from cortex.config import STIGMERGY_ENABLED
        if not STIGMERGY_ENABLED:
            return "Stigmergy: disabled."

        if token:
            c = get_consensus(token)
            return (
                f"Stigmergy ({token}): direction={c.direction}, "
                f"conviction={c.conviction:.2f}, sources={c.num_sources}, "
                f"swarm={'ACTIVE' if c.swarm_active else 'inactive'}, "
                f"bullish={c.bullish_weight:.2f}, bearish={c.bearish_weight:.2f}"
            )

        snap = get_board_snapshot()
        if snap["total_tokens"] == 0:
            return "Stigmergy: no signals deposited."
        lines = [f"Stigmergy Board: {snap['total_tokens']} token(s)"]
        for tk, data in list(snap["tokens"].items())[:5]:
            lines.append(
                f"  {tk}: {data['direction']} conviction={data['conviction']:.2f} "
                f"sources={data['num_sources']} swarm={'Y' if data['swarm_active'] else 'N'}"
            )
        return "\n".join(lines)
    except Exception:
        return "Stigmergy data unavailable."


def _collect_cascade_context(token: str = "") -> str:
    """Collect Ising cascade risk assessment."""
    try:
        from cortex.ising_cascade import get_cascade_score
        from cortex.config import ISING_CASCADE_ENABLED
        if not ISING_CASCADE_ENABLED:
            return "Ising Cascade: disabled."

        if not token:
            return "Ising Cascade: no token specified."

        result = get_cascade_score(token)
        return (
            f"Ising Cascade ({token}): risk={result.cascade_risk}, "
            f"score={result.cascade_score}/100, "
            f"magnetization={result.magnetization:.3f}, "
            f"temperature={result.effective_temperature:.3f}, "
            f"susceptibility={result.susceptibility:.3f}, "
            f"herding={result.herding_direction}"
        )
    except Exception:
        return "Ising Cascade data unavailable."


def _collect_memory_context(agent_id: str = "cortex") -> str:
    """Collect agent memory summary."""
    try:
        from cortex.agent_memory import get_context_snapshot
        snap = get_context_snapshot(agent_id)
        lt = snap.get("long_term", {})
        st = snap.get("short_term", [])

        if lt.get("total_decisions", 0) == 0:
            return f"Agent Memory ({agent_id}): no decisions recorded."

        lines = [
            f"Agent Memory ({agent_id}): {lt['total_decisions']} decisions",
            f"  Win rate: {lt.get('win_rate', 0):.1%}, "
            f"Avg risk: {lt.get('avg_risk_score', 0):.1f}, "
            f"PnL: {lt.get('cumulative_pnl', 0):+.2f}",
            f"  Dominant regime: {lt.get('dominant_regime', 'N/A')}",
        ]
        if st:
            last = st[-1]
            lines.append(
                f"  Last decision: {last.get('token', '?')} {last.get('direction', '?')} "
                f"score={last.get('score', 0):.1f} approved={last.get('approved', '?')}"
            )
        return "\n".join(lines)
    except Exception:
        return "Agent Memory data unavailable."


def _collect_vault_context(vault_id: str = "") -> str:
    """Collect vault delta features and risk score."""
    try:
        from cortex.vault_delta import get_vault_features, get_tracker
        from cortex.config import VAULT_DELTA_ENABLED
        if not VAULT_DELTA_ENABLED:
            return "Vault Delta: disabled."

        if vault_id:
            feat = get_vault_features(vault_id)
            d = feat.get("details", {})
            return (
                f"Vault Delta ({vault_id}): score={feat['score']}/100, "
                f"TVL vol={d.get('tvl_volatility_score', 0):.1f}, "
                f"SP vol={d.get('share_price_volatility_score', 0):.1f}, "
                f"flow={d.get('flow_score', 0):.1f}, "
                f"events={d.get('events', [])}"
            )

        tracker = get_tracker()
        all_deltas = tracker.get_all_deltas()
        if not all_deltas:
            return "Vault Delta: no vaults tracked."
        lines = [f"Vault Delta: {len(all_deltas)} vault(s)"]
        for vid, delta in list(all_deltas.items())[:5]:
            lines.append(
                f"  {vid}: TVL 24h={delta.tvl_24h_pct:+.1f}%, "
                f"SP 24h={delta.share_price_24h_pct:+.1f}%, "
                f"events={delta.events or 'none'}"
            )
        return "\n".join(lines)
    except Exception:
        return "Vault Delta data unavailable."


def _collect_override_context() -> str:
    """Collect active human overrides."""
    try:
        from cortex.human_override import list_active_overrides, get_registry
        from cortex.config import HUMAN_OVERRIDE_ENABLED
        if not HUMAN_OVERRIDE_ENABLED:
            return "Human Override: disabled."

        active = list_active_overrides()
        if not active:
            return "Human Override: no active overrides."

        lines = [f"Human Overrides: {len(active)} active"]
        for ovr in active[:5]:
            lines.append(
                f"  [{ovr['action']}] {ovr['token']} — {ovr['reason']} "
                f"(by {ovr['created_by']}, TTL {ovr.get('ttl_remaining', 0):.0f}s)"
            )

        audit = get_registry().get_audit_log(n=3)
        if audit:
            lines.append("  Recent audit:")
            for a in audit:
                lines.append(f"    {a['event']} {a.get('action', '')} {a.get('token', '')} @ {a.get('ts', 0):.0f}")

        return "\n".join(lines)
    except Exception:
        return "Human Override data unavailable."


